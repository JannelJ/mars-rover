package org.example;

public class PlateauSize {

    private int width; // 5
    private int height; // 5


    public PlateauSize(int width, int height) {
        this.width = width;
        this.height = height;
    }


    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}

